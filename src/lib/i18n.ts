export function t(key: string, locale: Locale = 'en'): string {
  return translations[locale]?.[key] || translations.en[key] || key;
}

/** Server-side: read locale from cookies */
export async function getServerLocale(): Promise<Locale> {
  try {
    const { cookies } = await import('next/headers');
    const store = await cookies();
    const val = store.get('admin-locale')?.value as Locale;
    return val && translations[val] ? val : 'en';
  } catch {
    return 'en';
  }
}

export function getLocale(): Locale {
  if (typeof window === 'undefined') return 'en';
  return (localStorage.getItem('admin-locale') as Locale) || 'en';
}

export function setLocale(locale: Locale) {
  localStorage.setItem('admin-locale', locale);
}
