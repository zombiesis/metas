function NavIcon({ d }: { d: string }) {
  return <svg width="16" height="16" fill="none" stroke="currentColor" strokeWidth="1.5" viewBox="0 0 24 24"><path strokeLinecap="round" strokeLinejoin="round" d={d}/></svg>;
}

function NavSection({ label, items, pathname, onNav }: { label: string; items: string[][]; pathname: string; onNav: () => void }) {
  const [open, setOpen] = useState(() => items.some(([, href]) => pathname === href || pathname.startsWith(href + '/')));
  const t = useT();
  return (
    <div className="nav-section">
      <button className={`nav-section-toggle ${open ? 'open' : ''}`} type="button" onClick={() => setOpen(!open)}><span className="nav-section-label">{label}</span><span className="nav-chevron">{open ? '▾' : '▸'}</span></button>
      {open && <div className="nav-section-items">{items.map(([lbl, href, icon]) => {
        const translated = t(lbl.toLowerCase().replace(/ /g, '_')) || lbl;
        return (
          <Link key={href} href={href} className={pathname === href || (href !== '/admin/dashboard' && pathname.startsWith(href + '/')) ? 'active' : ''} aria-current={pathname === href ? 'page' : undefined} onClick={onNav}>
            <NavIcon d={icon} /><span className="nav-label">{translated}</span>
          </Link>
        );
      })}</div>}
    </div>
  );
}

export function AdminChrome({ title, eyebrow = 'Enterprise CMS', description, user, children }: { title: string; eyebrow?: string; description?: string; user?: { username: string; roleName: string }; children: ReactNode }) {
  const pathname = usePathname();
  const [mobileOpen, setMobileOpen] = useState(false);
  const [isMini, setIsMini] = useState(false);
  const t = useT();

  useEffect(() => {
    const saved = localStorage.getItem('sidebar-mini');
    if (saved === 'true') setIsMini(true);
  }, []);

  const toggleSidebar = () => {
    if (typeof window !== 'undefined' && window.innerWidth <= 1180) {
      setMobileOpen(!mobileOpen);
    } else {
      setIsMini((prev) => {
        const next = !prev;
        localStorage.setItem('sidebar-mini', String(next));
        return next;
      });
    }
  };

  const onNavClick = () => {
    if (typeof window !== 'undefined' && window.innerWidth <= 1180) {
      setMobileOpen(false);
    }
  };

  return (
    <section className={`admin-app ${isMini ? 'desktop-mini' : ''} ${mobileOpen ? 'mobile-open' : ''}`} suppressHydrationWarning>
      <a href="#admin-content" className="skip">Skip to content</a>
      <aside className="admin-sidebar" aria-label="Admin navigation">
        <div className="admin-sidebar-brand">
          <div className="sidebar-logo">M</div>
          {!isMini && (
            <div style={{flex: 1}}>
              <strong>Metas CMS</strong>
              <span>Enterprise Platform</span>
            </div>
          )}
          <button className="sidebar-close-btn" type="button" onClick={() => setMobileOpen(false)} aria-label="Close menu">
            <svg width="20" height="20" fill="none" stroke="currentColor" strokeWidth="2"><path strokeLinecap="round" strokeLinejoin="round" d="M6 18L18 6M6 6l12 12" /></svg>
          </button>
        </div>
        <button className="sidebar-search" type="button" onClick={() => (window as any).__openCommandPalette?.()} title="Search">
          <svg width="14" height="14" fill="none" stroke="currentColor" strokeWidth="1.5" viewBox="0 0 24 24"><path strokeLinecap="round" strokeLinejoin="round" d="M21 21l-5.197-5.197m0 0A7.5 7.5 0 105.196 5.196a7.5 7.5 0 0010.607 10.607z"/></svg>
          {!isMini && <span>Search...</span>}
          {!isMini && <kbd>⌘K</kbd>}
        </button>
        
        {isMini ? (
          <nav className="admin-sidebar-mini-nav">
            {navSections.flatMap(s => s.items).map(([lbl, href, icon]) => {
              const translated = t(lbl.toLowerCase().replace(/ /g, '_')) || lbl;
              return (
                <Link key={href} href={href} title={translated} className={pathname === href || (href !== '/admin/dashboard' && pathname.startsWith(href + '/')) ? 'active' : ''} onClick={onNavClick}>
                  <NavIcon d={icon} />
                </Link>
              );
            })}
          </nav>
        ) : (
          <nav>
            {navSections.map((section) => (
              <NavSection key={section.label} label={section.label} items={section.items} pathname={pathname} onNav={onNavClick} />
            ))}
          </nav>
        )}

        {user && (
          <div className="sidebar-user" title={isMini ? user.username : undefined}>
            <div className="sidebar-avatar">{user.username[0].toUpperCase()}</div>
            {!isMini && (
              <div>
                <strong>{user.username}</strong>
                <span>{user.roleName}</span>
              </div>
            )}
          </div>
        )}

        {/* Desktop collapse toggle arrow — hidden on mobile */}
        <button
          className="sidebar-collapse-btn"
          type="button"
          onClick={() => setIsMini((prev) => {
            const next = !prev;
            localStorage.setItem('sidebar-mini', String(next));
            return next;
          })}
          aria-label={isMini ? 'Expand sidebar' : 'Collapse sidebar'}
          title={isMini ? 'Expand sidebar' : 'Collapse sidebar'}
        >
          <svg width="14" height="14" fill="none" stroke="currentColor" strokeWidth="2.5" viewBox="0 0 24 24">
            <path strokeLinecap="round" strokeLinejoin="round" d={isMini ? 'M9 5l7 7-7 7' : 'M15 19l-7-7 7-7'} />
          </svg>
        </button>
      </aside>
      {mobileOpen && <div className="admin-overlay" onClick={() => setMobileOpen(false)} />}
      <div className="admin-main" id="admin-content">
        <header className="admin-topbar">
          <div>
            <button className="admin-menu-toggle" type="button" onClick={toggleSidebar} aria-label="Toggle menu">
              <svg width="24" height="24" fill="none" stroke="currentColor" strokeWidth="2"><path d="M4 6h16M4 12h16M4 18h16" /></svg>
            </button>
            <Breadcrumb />
            <h1>{title}</h1>
            {description ? <p>{description}</p> : null}
          </div>
          <div className="admin-user">
            <BranchSwitcher />
            <LanguageToggle />
            <NotificationBell />
            <DarkModeToggle />
            <Link className="btn outline" href="/">{t('view_site')}</Link>
            <form method="post" action="/api/admin/logout"><button className="btn gold">{t('logout')}</button></form>
          </div>
        </header>
        <AdminErrorBoundary>
          {children}
        </AdminErrorBoundary>
        <OnboardingTips />
        <AdminNotifications />
        <CommandPalette />
      </div>
    </section>
  );
}
